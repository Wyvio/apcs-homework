package iLoveToWriteCode;

public class main {

	public static void main(String[] args) {
		printAgain();
		printAgain();
		printAgain();
		printAgain();
	}
	
	public static void print() {
		System.out.println("I love to write code");
	}
	
	public static void printOut() {
		print();
		print();
		print();
		print();
		print();
	}
	
	public static void printAgain() {
		printOut();
		printOut();
		printOut();
		printOut();
		printOut();
	}
}
